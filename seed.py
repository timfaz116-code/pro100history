def seed():
    with app.app_context():
        db.drop_all()
        db.create_all()

        test_user = User(
            username='Ученик',
            email='student@example.com',
            role='student',
        )
        test_user.set_password('123456')
        db.session.add(test_user)

        for lesson_data in LESSONS_DATA:
            lesson = Lesson(
                date=lesson_data['date'],
                time=lesson_data['time'],
                topic=lesson_data['topic_name'],
                format=lesson_data['format'],
                status=lesson_data['status'],
                description=lesson_data['description'],
            )
            db.session.add(lesson)

        topic_map = {}
        for td in TOPICS_DATA:
            topic = Topic(
                name=td['name'],
                description=td['description'],
                materials_count=td['materials_count'],
                tasks_count=td['tasks_count'],
                sort_order=td['sort_order'],
            )
            db.session.add(topic)
            topic_map[td['name']] = topic

        db.session.flush()

        for td in TASKS_DATA:
            topic = topic_map.get(td['topic_name'])
            if not topic:
                continue
            for task_data in td['tasks']:
                task = Task(
                    topic_id=topic.id,
                    question=task_data['question'],
                    options=json.dumps(task_data['options'], ensure_ascii=False),
                    correct_answer=task_data['correct_answer'],
                    explanation=task_data['explanation'],
                )
                db.session.add(task)

        db.session.commit()
        print('База данных успешно заполнена!')
        print('Тестовый пользователь: student@example.com / 123456')


if __name__ == '__main__':
    seed()
